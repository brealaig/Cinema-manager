    private boolean crearUsuario(){
        
        UsuarioDB usuarioDB = UsuarioDB.getConexion();
        
        boolean usuarioCreado;
        
        actualizarDatos();
        
        FachadaCreacionUsuario fachada = FachadaCreacionUsuario.getFachada().getFachada();
        ComandoCreacionUsuario comando;
        Invocador invocador = new Invocador();
        
        if(validarDatos() == true && verificarExistencia() == false){
            comando = new ComandoCreacionUsuario(fachada,nombre,identificacion,correo,contrasena,"Estandar",0);
            
            invocador.ejecutar(comando);

            System.out.println("Usuario "+usuarioDB.getUsuario(usuarioDB.getBaseDeDatos().size()-1).getNombre()+" creado");
            
            return true;
        }
        else{
            return false;
        }
    }
    
    private void actualizarDatos(){
        this.nombre = jTextField1.getText();
        this.identificacion = Integer.parseInt(jTextField2.getText());
        this.correo = jTextField3.getText();
        this.contrasena = jPasswordField1.getText();
        this.contrasenaVerif = jPasswordField2.getText();
    }
    
    private boolean validarDatos(){
        if(nombre.equals("") || identificacion == 0 || correo.equals("") || contrasena.equals("") || !contrasena.equals(contrasenaVerif)){
            return false;
        }
        else{
            return true;
        }
    }
    
    private boolean verificarExistencia(){
        UsuarioDB usuarioDB = UsuarioDB.getConexion();
        
        boolean existe = false;
        
        for (int i = 0; i<usuarioDB.getBaseDeDatos().size(); i++){
            if(identificacion == usuarioDB.getUsuario(i).getIdentificacion() && usuarioDB.getUsuario(i).getTipo().equals("Estandar")){
                existe = true;
                System.err.println("Ya existe un usuario con este documento");
            }
        }
        
        return existe;
    }

